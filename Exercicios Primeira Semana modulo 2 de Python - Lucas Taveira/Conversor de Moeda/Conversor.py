def conversor_dolar_euro(valor):
    try:
        print(f"\n\n1 Dólar americano igual a 0.97 Euro")
        print(f"{valor} \t| Dólar americano")
        valor = valor * 0.97
        print(f"{valor} \t| Euro\n")
    except:
        print("Algo deu errado na conversão da moeda!")
        return
    
